public class characters{
    public static void main(String args[]){


System.out.println(Character.isDigit('j'));
System.out.println(Character.isDigit('5'));
System.out.println(Character.isDigit('%'));



System.out.println(Character.isLetter('j'));
System.out.println(Character.isLetter('5'));
System.out.println(Character.isLetter('%'));



System.out.println(Character.isLetterOrDigit('x'));
System.out.println(Character.isLetterOrDigit('7'));
System.out.println(Character.isLetterOrDigit('&'));



System.out.println(Character.isLowerCase('x'));
System.out.println(Character.isUpperCase('X'));
System.out.println(Character.toLowerCase('u'));
System.out.println(Character.toUpperCase('U'));




    }
}